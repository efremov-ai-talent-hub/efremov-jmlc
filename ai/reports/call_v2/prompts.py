def build_chain_context_prompt(
    previous_reports: Iterable[dict[str, Any]],
    *,
    current_chain_position: int | None,
) -> str:
    """Build a prefix block with summaries of previous chain calls to give the
    main-pass LLM the context of the whole conversation with the client.

    Only reports that were filtered as suitable (non-junk, no connection_lost)
    should be passed in. The block ends with a newline-separated boundary so the
    caller can concatenate it before the original SYSTEM_PROMPT."""

    lines: list[str] = []
    if current_chain_position is not None:
        lines.append(
            f"КОНТЕКСТ ЦЕПОЧКИ: это звонок #{int(current_chain_position)} в цепочке с клиентом."
        )
    else:
        lines.append("КОНТЕКСТ ЦЕПОЧКИ: это продолжение цепочки звонков с клиентом.")

    lines.append("Краткое содержание предыдущих звонков этой цепочки:")

    for report in previous_reports:
        payload = report.get("payload") if isinstance(report, dict) else None
        summary: str | None = None
        if isinstance(payload, dict):
            summary = payload.get("summary")
        if not summary and isinstance(report, dict):
            summary = report.get("summary")
        if not summary:
            continue
        position = report.get("chain_position") if isinstance(report, dict) else None
        call_type = report.get("call_type") if isinstance(report, dict) else None
        prefix_parts: list[str] = []
        if position is not None:
            prefix_parts.append(f"#{int(position)}")
        if call_type:
            prefix_parts.append(f"тип: {call_type}")
        prefix = f"[{', '.join(prefix_parts)}]" if prefix_parts else "[предыдущий звонок]"
        lines.append(f"{prefix} {summary}")

    lines.append(
        "Учитывай этот контекст при анализе текущего звонка: уже выясненные потребности, "
        "данные клиента и договорённости не надо повторно требовать от менеджера в текущем звонке."
    )
    lines.append("")
    return "\n".join(lines)

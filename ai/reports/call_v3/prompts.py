# --- QC: контракт на evidence переписан ------------------------------------------------------
# Замерено (20 звонков, 243 qc-цитаты): дословных 7%, выдумано 44% — при том что стадии с явным
# требованием дословности дают 61–97%. Причина не в модели: правило 2 промпта v2 САМО просит
# прозу («Если score=0 — в evidence объясни, чего именно не было»), а проваленных критериев в
# звонке большинство. Модель послушно писала вердикт своими словами, и преамбула этого не
# перебивала — инструкция-надстройка слабее тела промпта.
#
# Поэтому v3 не уговаривает, а меняет контракт: цитата либо есть, либо стоит явный маркер
# «н/п — не прозвучало» (проверки читают его как «нечего оценивать», а не как выдумку). Замена
# точечная и с проверкой: v2 заморожен, но если его текст всё же уедет — сборка промпта упадёт,
# а не начнёт молча слать неизменённое правило.

_V2_EVIDENCE_RULE = (
    "2) evidence обязателен для каждого критерия: цитата из транскрипта "
    "(дословно или близко к тексту).\n"
    "   Если score=0 — в evidence объясни, чего именно не было."
)

_V3_EVIDENCE_RULE = (
    "2) У каждого критерия ДВА поля, и они не взаимозаменяемы:\n"
    "   quotes — МАССИВ подтверждений. Каждый элемент: quote (ДОСЛОВНЫЕ слова из транскрипта, "
    "скопируй как есть, без таймкода и без метки спикера) и timecode реплики, откуда цитата.\n"
    "   Сколько элементов — решает критерий: обычно одно; НЕСКОЛЬКО там, где надо доказать "
    "количество (обращения по имени, преимущества проекта, возражения). Если подтверждать "
    "нечем — верни ПУСТОЙ массив []. Не пиши в quotes ни пересказ, ни «н/п», ни пустую строку.\n"
    "   comment — твой вердикт своими словами. Весь пересказ и все пояснения идут СЮДА.\n"
    "   У критериев general_polite_tone, general_professional_speech, general_initiative_control, "
    "general_active_listening quotes ВСЕГДА пустой: они про разговор целиком, отдельной "
    "подтверждающей фразы у них не бывает — вердикт пиши в comment.\n"
)

# Два критерия по контракту отдают перечень, а не одну цитату. Требуем, чтобы перечень состоял
# из цитат: иначе они остаются единственной законной дырой для пересказа.
_V2_LIST_RULES = (
    (
        "В evidence перечисли каждое найденное преимущество.",
        "В quotes — по одному элементу на КАЖДОЕ найденное преимущество, дословной цитатой.",
    ),
    (
        "В evidence кратко перечисли каждое возражение и статус.",
        "В quotes — по одному элементу на КАЖДОЕ возражение клиента, дословной цитатой; "
        "статус каждого — в comment.",
    ),
)


def qc_system_prompt() -> str:
    """Промпт qc-критериев v2 с переписанным контрактом на evidence.

    v2 заморожен и импортируется как есть; правится ровно то, что делает evidence прозой.
    """
    prompt = QC_SYSTEM_PROMPT
    for old, new in ((_V2_EVIDENCE_RULE, _V3_EVIDENCE_RULE), *_V2_LIST_RULES):
        if old not in prompt:
            raise RuntimeError(f"v2 qc prompt changed: fragment not found: {old[:60]!r}")
        prompt = prompt.replace(old, new)
    return prompt
